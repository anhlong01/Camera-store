const useProducts = () =>{
    const[products, setProducts] = useState([]);
    
    useEffect(() => {
        const getProducts = async ()=>{
            const data = await new productDataService().getAllProducts();
            setProducts(data.docs.map((doc) => ({ ...doc.data(), id: doc.id })));
        };

        getProducts();
    }, []);

    return products;
    
}

export default useProducts;